SARUS_SUITE_PODMAN_ROOT_PLACEHOLDER='@@SARUS_SUITE_PODMAN_ROOT@@'
SARUS_SUITE_PODMAN_RUNROOT_PLACEHOLDER='@@SARUS_SUITE_PODMAN_RUNROOT@@'
