RUNTIME_HOOK_BIN_DIR="${RUNTIME_HOOK_BIN_DIR:-${RUNTIME_LIBEXEC_DIR}/oci/hooks}"
RUNTIME_CONTAINERS_ETC_DIR="${RUNTIME_CONTAINERS_ETC_DIR:-${BUNDLE_ROOT}/etc/containers}"
RUNTIME_CONTAINERS_MODULES_DIR="${RUNTIME_CONTAINERS_MODULES_DIR:-${RUNTIME_CONTAINERS_ETC_DIR}/containers.conf.modules}"
RUNTIME_CONTAINERS_HOOKS_DIR="${RUNTIME_CONTAINERS_HOOKS_DIR:-${RUNTIME_CONTAINERS_ETC_DIR}/oci/hooks.d}"
RUNTIME_PARALLAX_ETC_DIR="${RUNTIME_PARALLAX_ETC_DIR:-${BUNDLE_ROOT}/etc/parallax}"
RUNTIME_SARUS_SUITE_ETC_DIR="${RUNTIME_SARUS_SUITE_ETC_DIR:-${BUNDLE_ROOT}/etc/sarus-suite}"
RUNTIME_SHARE_DIR="${RUNTIME_SHARE_DIR:-${BUNDLE_ROOT}/share}"
RUNTIME_LICENSE_DIR="${RUNTIME_LICENSE_DIR:-${RUNTIME_SHARE_DIR}/licenses}"
RUNTIME_MANIFEST="${RUNTIME_MANIFEST:-${RUNTIME_SHARE_DIR}/manifest.txt}"

SARUS_SUITE_BIN_PLACEHOLDER='@@SARUS_SUITE_BIN@@'
SARUS_SUITE_HOOK_BIN_PLACEHOLDER='@@SARUS_SUITE_HOOK_BIN@@'
