LIBFUSE_VERSION="${LIBFUSE_VERSION:-3.18.2}"

MKSQUASHFS_BIN="${MKSQUASHFS_BIN:-${BUILD_DIR}/mksquashfs}"
RSYNC_BIN="${RSYNC_BIN:-${BUILD_DIR}/rsync}"
INOTIFYWAIT_BIN="${INOTIFYWAIT_BIN:-${BUILD_DIR}/inotifywait}"
SQUASHFUSE_LL_BIN="${SQUASHFUSE_LL_BIN:-${BUILD_DIR}/squashfuse_ll}"
FUSE_OVERLAYFS_BIN="${FUSE_OVERLAYFS_BIN:-${BUILD_DIR}/fuse-overlayfs}"
FUSERMOUNT3_BIN="${FUSERMOUNT3_BIN:-${BUILD_DIR}/fusermount3}"

RUNTIME_BIN_DIR="${RUNTIME_BIN_DIR:-${BUNDLE_ROOT}/bin}"
